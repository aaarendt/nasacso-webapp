export const spacingHorizontal = spacing => `0 ${spacing} 0 ${spacing}`
export const spacingVertical = spacing => `${spacing} 0 ${spacing} 0`
export const spacingTop = spacing => `${spacing} 0 0 0`
export const spacingRight = spacing => `0 ${spacing} 0 0`
export const spacingBottom = spacing => `0 0 ${spacing} 0`
export const spacingLeft = spacing => `0 0 0 ${spacing}`
export const spacingBottomLeft = spacing => `0 0 ${spacing} ${spacing}`
export const spacingBottomRight = spacing => `0 ${spacing} ${spacing} 0`
export const spacingTopLeft = spacing => `${spacing} 0 0 ${spacing}`
export const spacingTopRight = spacing => `${spacing} ${spacing} 0 0`